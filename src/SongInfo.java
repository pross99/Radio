public class SongInfo {

    String name;
    String artist;
    int yearReleased;

    public SongInfo(String name, String artist, int yearReleased) {
        this.name = name;
        this.artist = artist;
        this.yearReleased = yearReleased;
    }


    public String getName() {
        return name;
    }


    public String getArtist() {
        return artist;
    }

    public int getYearReleased() {
        return yearReleased;
    }









}
